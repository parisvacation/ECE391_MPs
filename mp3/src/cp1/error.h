//           error.h - Error numbers
//           

#ifndef _ERROR_H_
#define _ERROR_H_

#define EINVAL      1   // invalid argument
#define EBUSY       2   // a device or resource is currently in use or locked
#define ENOTSUP     3   // operation not supported
#define ENODEV      4   // no such device
#define EIO         5   // a read or write operation failed
#define EBADFMT     6   // a file or data format is incorrect
#define ENOENT      7   // no such file or directory

//           _ERROR_H_
#endif
